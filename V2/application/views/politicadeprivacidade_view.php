                <p>Nós da empresa <strong>NOLA PARTICIPAÇÕES E ADMINISTRAÇÃO DE BENS PROPRIOS LTDA</strong> respeitamos a privacidade de todos os nossos
                    clientes, colaboradores e parceiros, nosso objetivo é garantir a integridade das informações
                    e resguardar a privacidade.</p>

                <p>A presente Política de Privacidade tem por finalidade demonstrar o nosso compromisso com a
                    privacidade e a proteção dos dados pessoais coletados, estabelecendo as regras sobre a coleta,
                    registro, armazenamento, uso, compartilhamento e eliminação dos dados pessoais coletados.</p>

                <p><strong>1. Informações Coletadas</strong></p>
                <p>Informações fornecidas por você: coletamos as informações que você fornece quando realiza
                    consultas ou adquire produtos e/ou serviços através dos seguintes canais: site, redes
                    sociais, stand de vendas, contato pessoal, por telefone, email e contratos firmados.
                    Caso você não queira fornecer as informações solicitadas (obrigatórias ou não), talvez
                    você não consiga aproveitar plenamente os nossos serviços. Tais informações poderão
                    incluir o seu nome, endereço, e-mail, telefone, documentos de identificação, dados
                    bancários e de renda.</p>

                <p><i>Informações coletadas automaticamente</i>: Quando você usar este site, o servidor poderá
                    reconhecê-lo automaticamente através de um cookie ou outra tecnologia de rastreamento.
                    Essas tecnologias atuam como identificadores únicos, permitindo-nos ajustar o
                    site aos interesses especificados por você e rastrear a sua atividade. Essas
                    informações poderão ser adicionadas ao seu perfil de usuário e usadas para
                    enviar ofertas e anúncios que acreditamos serem relevantes para os seus interesses.</p>

                <p><strong>2. Uso de Informações</strong></p>
                <p>Poderemos usar as informações coletadas sobre você para: responder suas consultas e solicitações;
                    manter você informado sobre nossos serviços através de SMS, MMS, mala-direta, aplicativos de comunicação
                    (como, por exemplo, o WhatsApp), entre outros recursos; desenvolver novos produtos e aperfeiçoar/melhorar
                    nosso negócio; e usar os seus dados para fins analíticos e estatísticos.<br>
                    Contamos algumas bases legais para coletar e processar suas informações: (a) mediante consentimento; (b)
                    cumprimento de obrigação legal; (c) para execução de contrato ou de procedimentos preliminares; (c) para o
                    exercício regular de direitos em processo judicial, administrativo ou arbitral; e (d) atender aos interesses
                    legítimos da empresa.</p>

                <p><strong>3. Compartilhamento de Informações</strong></p>
                <p>Além disso, poderemos compartilhar suas informações para: (a) atender processos judiciais; (b)
                    responder a solicitações legais feitas por autoridades; (c) operações societárias (fusão, aquisição ou venda);
                    (d) auditorias; e (e) para fornecedores quando for imprescindível para a execução dos serviços. Não
                    compartilhamos com terceiros interessados em vender serviços/produtos, nem tampouco autorizamos
                    usarem suas informações.</p>

                <p><strong>4. Segurança</strong></p>
                <p>São adotadas todas as medidas técnicas plausíveis do estado atual da tecnologia, a fim de garantir a
                    segurança dos dados pessoais e a evitar a sua alteração, perda, tratamento e o acesso não autorizados,
                    quer provenham de ação humana, quer derivem de ações naturais, físicas ou eletrônicas.</p>

                <p><strong>5. Seus Direitos de Privacidade</strong></p>
                <p>Você tem direito, quando aplicável, a obter mediante requisição: confirmação da existência de tratamento;
                    acesso aos dados; correção de dados incompletos, inexatos ou desatualizados; anonimização,
                    bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade
                    com a legislação; portabilidade dos dados a outro fornecedor de serviço ou produto, mediante
                    requisição expressa, de acordo com a regulamentação da autoridade nacional, observados os
                    segredos comercial e industrial; eliminação dos dados pessoais tratados com o consentimento
                    do titular; informação das entidades públicas e privadas com as quais o controlador realizou
                    uso compartilhado de dados; informação sobre a possibilidade de não fornecer consentimento e
                    sobre as consequências da negativa; e revogação do consentimento. Para exercer esses direitos
                    ou qualquer outra dúvida referente a essa Política de Privacidade entre em contato com o nosso
                    Encarregado de Dados Pessoais.</p>

                <p><strong>6. Período de Retenção e Eliminação</strong></p>
                <p>As suas informações pessoais serão mantidas pelo prazo necessário até o fiel cumprimento dos
                    nossos serviços, exceto quando um período de retenção mais longo for exigido ou permitido de
                    acordo com a lei. Exaurido o prazo os dados serão devidamente eliminados.</p>

                <p><strong>7. Treinamento e Conscientização</strong></p>
                <p>Através de uma agenda de treinamentos e conscientização acerca do tema Privacidade e Proteção
                    de Dados a empresa fornece aos seus colaboradores todos os subsídios para garantir a devida
                    proteção dos seus dados pessoais.</p>

                <p><strong>8. Alterações na Política de Privacidade</strong></p>
                <p>Poderemos alterar esta Política de Privacidade regularmente.
                    Nessa ocasião, publicaremos uma versão atualizada.</p>

                <p><strong>9. Fale Conosco</strong></p>
                <p>Em caso de dúvidas sobre esta Política de Privacidade, entre em contato conosco no
                    endereço de e-mail <strong>contato@tonsdobras.com.br</strong>.</p>
